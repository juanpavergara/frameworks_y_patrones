package gof.observer;

public class EventoDeNegocioDTO {
	
	private String datoA;
	private int datoB;
	
	public String getDatoA() {
		return datoA;
	}
	public void setDatoA(String datoA) {
		this.datoA = datoA;
	}
	public int getDatoB() {
		return datoB;
	}
	public void setDatoB(int datoB) {
		this.datoB = datoB;
	}
	
	
}
